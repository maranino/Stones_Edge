
class Inventory:
    """Class to represent the inventory of the player."""
    def __init__(self, armor, weapon, hp_potion, mp_potion, drops, gathers, gift):
        self.armor = armor
        self.weapon = weapon
        self.hp_potion = hp_potion
        self.mp_potion = mp_potion
        self.drops = drops
        self.gathers = gathers
        self.gift = gift

